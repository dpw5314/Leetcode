class Solution {
public:
    int arrayPairSum(vector<int>& nums) {
        multiset<int> temp;
        int ans = 0;
        for(auto ele : nums)
            temp.insert(ele);

        for(auto it = temp.begin(); it != temp.end(); ++it){
            ans += *it;
            ++it;
        }
        return ans;
    }
};


